// ==================== INTERFACES ====================

/**
 * Interface Movie - mendefinisikan struktur data setiap film
 * Digunakan sebagai tipe data utama dalam seluruh aplikasi
 */
export interface Movie {
  id: number;
  title: string;
  genre: string[];
  year: number;
  rating: number;
  duration: number; // dalam menit
  popularity: number; // skala 1-100
  description: string;
  posterUrl: string; // gradient placeholder
}

/**
 * Interface UserPreferences - preferensi pengguna untuk rekomendasi
 * Digunakan oleh algoritma scoring untuk menghitung skor rekomendasi
 */
export interface UserPreferences {
  favoriteGenres: string[];
  minRating: number;
  minYear: number;
  maxDuration: number;
}

/**
 * Interface RecommendationResult - hasil rekomendasi dengan skor dan alasan
 */
export interface RecommendationResult {
  movie: Movie;
  score: number;
  reasons: string[];
}

